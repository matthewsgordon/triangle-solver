public class Triangle {
	private float a;
	private float b; 
	private float c;
	
	// Creates the triangle object, defines the value of a, b and c 
	public Triangle(float sa, float sb, float sc)
	{
		 a = sa;
		 b = sb;
		 c = sc;		
	}
	
	// Returns the value of a 
	public float getA()
	{
		return a;
	}
	
	// Sets the value of a to sa
	public void setA(float sa)
	{
		this.a = sa;
	}
	
	// Returns the value of b
	public float getB()
	{
		return b;
	}
	
	// Sets the value of b to sb
	public void setB(float sb)
	{
		this.b = sb;
	}
	
	// Returns the value of c
	public float getC()
	{
		return c;
	}
	
	// Sets the value of c to sc
	public void setC(float sc)
	{
		this.c = sc;
	}
	
	
	
	
	
	

}
